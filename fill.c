#include "fdf.h"

int		**fill(int **array, int fd, t_m m)
{
	char	*l;
	int		g;

	m.i = 0;
	m.j = 0;
	m.k = -1;
	while ((g = get_next_line(fd, &l)) == 1)
	{
		while (l[++m.k])
		{
			if (l[m.k] >= '0' && l[m.k] <= '9')
			{
				array[m.j][m.i] = l[m.k] - 1 == '-' ? (l[m.k] - 48) * -1 
					: l[m.k] - 48;
				m.i++;
			}
		}
		free(l);
		m.i = 0;
		m.j++;
		m.k = -1;
	}
	close(fd);
	return (array);
}
